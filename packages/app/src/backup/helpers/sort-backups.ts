import { Backup } from "App/backup/reducers"

export const sortBackups = (backups: Backup[]): Backup[] => {
  return [...backups].sort((a, b) => {
    const dateA = new Date(a.date).getTime()
    const dateB = new Date(b.date).getTime()
    return dateB - dateA
  })
}
